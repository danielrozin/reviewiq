/**
 * Get all blog posts sorted by published date (newest first)
 */
export function getAllBlogPosts(): BlogPost[] {
  return [...blogPosts].sort(
    (a, b) =>
      new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

/**
 * Get a single blog post by its slug
 */
export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find((post) => post.slug === slug);
}

/**
 * Get all blog posts for a given category slug
 */
export function getBlogPostsByCategory(categorySlug: string): BlogPost[] {
  return blogPosts
    .filter((post) => post.categorySlug === categorySlug)
    .sort(
      (a, b) =>
        new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    );
}

/**
 * Get the most recent blog posts, limited to a given count
 */
export function getRecentBlogPosts(limit: number): BlogPost[] {
  return getAllBlogPosts().slice(0, limit);
}

/**
 * Get all unique blog categories with slug and name
 */
export function getBlogCategories(): { slug: string; name: string }[] {
  const seen = new Map<string, string>();
  for (const post of blogPosts) {
    if (!seen.has(post.categorySlug)) {
      seen.set(post.categorySlug, post.categoryName);
    }
  }
  return Array.from(seen.entries()).map(([slug, name]) => ({ slug, name }));
}

