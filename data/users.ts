export function getUserById(id: string): UserProfile | undefined {
  return users.find((u) => u.id === id);
}

export function getUserByUsername(username: string): UserProfile | undefined {
  return users.find((u) => u.username === username);
}

export function getTopContributors(limit: number = 5): UserProfile[] {
  return [...users]
    .sort((a, b) => b.reputationScore - a.reputationScore)
    .slice(0, limit);
}

export function getContributorsByCategory(categorySlug: string): UserProfile[] {
  return users
    .filter((u) => u.expertiseCategories.includes(categorySlug))
    .sort((a, b) => b.reputationScore - a.reputationScore);
}
